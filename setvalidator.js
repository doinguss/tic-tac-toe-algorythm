var sets=[];

//from algorytm.tictactoe2.js
function trsp(b) {let o=structuredClone(b);for(let i=0;i<3;i){for(let j=0;j<3;j){   [o[i][j],o[j][i]]=[o[j][i],o[i][j]] ;}}return o;}//mirrors on diagonal
function rev (b) {let o=structuredClone(b);for(let i=0;i<3;i){for(let j=0;j<3;j){             o[i][j]=b[2-i][j]         ;}}return o;}//reverses the order in each row

//w111 10g 411 1n574nc35 0f r3p3471ng b04rd 574735 (wr173n 1n 13375p34k 4 funn5i35)
function validate(...mtrx){
    mtrx.forEach(Element=>{
        let o=structuredClone(Element);
        if (sets.includes(o)){ console.log(Element);return;}
        for (let i=0;i<4;i++){
            o=trsp(o);
            if (sets.includes(o)){ console.log(Element);return;}
            o=rev(o);
            if (sets.includes(o)){ console.log(Element);return;}
            o=trsp(o);
            if (sets.includes(o)){ console.log(Element);return;}
            o=trsp(o);
        }
        sets.push(Element);
    });

}
